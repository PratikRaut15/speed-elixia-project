 * @author Mrudang
 */
class LrResponseClass {
    function __construct($arrResponse) {

        $this->vehicleNo = isset($arrResponse->vehicleNo) ? $arrResponse->vehicleNo : '';
        $this->orderNo = isset($arrResponse->orderNo) ? $arrResponse->orderNo : '';
        $this->orderDateTime = isset($arrResponse->orderDateTime) ? $arrResponse->orderDateTime : '';
    }

    // <editor-fold defaultstate="collapsed" desc="Public Variables">

    public $vehicleNo;
    public $orderNo;
    public $orderDateTime;
    // </editor-fold>
}
