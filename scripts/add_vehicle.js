function isNumberKey(evt){
    var charCode = (evt.which) ? evt.which : event.keyCode
    if (charCode > 31 && (charCode < 48 || charCode > 57))
        return false;
    return true;
}

function nospecialchars(evt) {
    var charCode = (evt.which) ? evt.which : event.keyCode
    if ((charCode > 32 && charCode < 44) || charCode == 64)
        return false;
    return true;
}
        
