    public static function InchToCm( $inch, $decplaces = 2 )
    {
        return round( ($inch+0) * 2.54 , $decplaces);
    }

    public static function CmToInch( $cm, $decplaces = 2 )
    {
        return round( ($cm+0) *  0.393700787 , $decplaces);
    }

    public static function LbsToKg( $lbs, $decplaces = 2 )
